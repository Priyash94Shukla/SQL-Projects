-- Store Procedure
create a function as soon as the product is sold the the same quantity should reduced from inventory table
after adding any sales records it should update the stock in the inventory table based on the product and qty purchased
*/
CREATE OR REPLACE PROCEDURE add_sales
(
p_order_id INT,
p_customer_id INT,
p_seller_id INT,
p_order_item_id INT,
p_product_id INT,
p_quantity INT
)
LANGUAGE plpgsql
AS $$ 

DECLARE
v_count INT;
v_price FLOAT;
v_product VARCHAR(50);

BEGIN
-- Checking if the ordered product is available in the inventory 
	SELECT price, product_name
		INTO
		v_price,v_product
	FROM products	
	WHERE product_id = p_product_id;
	
	SELECT COUNT(*)
		   INTO
		   v_count
	FROM inventory
	WHERE product_id = p_product_id
		  AND stock >= p_quantity;

	IF v_count > 0 THEN 
	--We need to update the order/order_items/inventory tables as these tables gets affected after each complete order
		INSERT INTO orders(order_id,order_date,customer_id,seller_id)
		VALUES
		(p_order_id, CURRENT_DATE, p_customer_id, p_seller_id);

		INSERT INTO order_items(order_item_id,order_id,product_id,quantity,price_per_unit,total_sale)
		VALUES
		(p_order_item_id,p_order_id,p_product_id,p_quantity,v_price, v_price*p_quantity);

		UPDATE inventory
		SET stock = stock - p_quantity
		WHERE product_id = p_product_id;

		RAISE NOTICE 'Thank you for shopping. product: % sale is added and inventory is updated', v_product;

	ELSE 
		RAISE NOTICE 'Thankyou for your time unfortunately the product : % is not available', v_product;

	END IF;

END;
$$

