/*
2. Revenue by Category
Calculate total revenue generated by each product category.
Challenge: Include the percentage contribution of each category to total revenue.
*/
-- Method-I (With CTE)
WITH CTE AS (
  SELECT 
    SUM(total_sale) AS s_t 
  FROM 
    order_items
) 
SELECT 
  c.category_name, 
  SUM(oi.total_sale) AS category_sale, 
  (
    SUM(oi.total_sale)/ t.s_t
  ) * 100 AS percentage_contribution 
FROM 
  order_items AS oi 
  JOIN products AS p ON oi.product_id = p.product_id 
  JOIN category AS c ON p.category_id = c.category_id CROSS 
  JOIN CTE AS t 
GROUP BY 
  c.category_name, 
  t.s_t;


-- Method-II (With Subquery)

SELECT 
  c.category_name, 
  SUM(oi.total_sale) AS category_sale, 
  (
    SUM(oi.total_sale)/(
      SELECT 
        SUM(total_sale) 
      FROM 
        order_items
    )
  ) * 100 AS percentage_contribution 
FROM 
  order_items AS oi 
  JOIN products AS p ON oi.product_id = p.product_id 
  JOIN category AS c ON p.category_id = c.category_id 
GROUP BY 
  1;

/*
3. Average Order Value (AOV)
Compute the average order value for each customer.
Challenge: Include only customers with more than 5 orders.
*/
--Method-I (Using simple join)
SELECT 
  c.customer_id, 
  c.first_name, 
  c.last_name, 
  SUM(oi.total_sale)/ COUNT(o.order_id) AS AOV, 
  COUNT(o.order_id) AS order_counts 
FROM 
  customers AS c 
  JOIN orders AS o ON c.customer_id = o.customer_id 
  JOIN order_items AS oi ON o.order_id = oi.order_id 
GROUP BY 
  1, 
  2, 
  3 
HAVING 
  COUNT(o.order_id) > 5 
ORDER BY 
  4 DESC;


-- Method-II (Using Common Table Expression)

WITH no_orders AS(
  SELECT 
    c.customer_id, 
    COUNT(o.customer_id) AS o_counts 
  FROM 
    customers AS c 
    JOIN orders AS o ON c.customer_id = o.customer_id 
  GROUP BY 
    1
) 
SELECT 
  c.customer_id, 
  c.first_name, 
  c.last_name, 
  SUM(oi.total_sale)/(no_orders.o_counts) AS AOV 
FROM 
  customers AS c 
  JOIN orders AS o ON c.customer_id = o.customer_id 
  JOIN order_items AS oi ON o.order_id = oi.order_id 
  JOIN no_orders ON c.customer_id = no_orders.customer_id 
GROUP BY 
  1, 
  2, 
  3, 
  no_orders.o_counts 
HAVING 
  no_orders.o_counts > 5 
ORDER BY 
  4 DESC;


/*
4. Monthly Sales Trend
Query monthly total sales over the past year.
Challenge: Display the sales trend, grouping by month, return current_month sale, last month sale!
*/

-- Note :- Since we are in 2025 and data's latest date is Jul-2024. We we cannot use interval function directly
WITH max_date AS (
  SELECT 
    MAX(order_date) AS max_dt 
  FROM 
    orders
) 
SELECT 
  order_year, 
  order_month, 
  monthly_sale, 
  LAG(monthly_sale) OVER(
    ORDER BY 
      order_year, 
      order_month
  ) 
FROM 
  (
    SELECT 
      EXTRACT(
        YEAR 
        FROM 
          o.order_date
      ) AS order_year, 
      EXTRACT(
        MONTH 
        FROM 
          o.order_date
      ) AS order_month, 
      ROUND(
        SUM(oi.total_sale):: numeric, 
        2
      ) AS monthly_sale 
    FROM 
      orders AS o 
      JOIN order_items AS oi ON o.order_id = oi.order_id 
      JOIN max_date AS md ON TRUE 
    WHERE 
      o.order_date >= md.max_dt - INTERVAL '12 MONTHS' 
    GROUP BY 
      1, 
      2 
    ORDER BY 
      1, 
      2
  );

	
